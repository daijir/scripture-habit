// Function to translate a book name from English to the user's language
export const translateBookName = (bookName, language) => {
    if (!bookName || !language) return bookName;

    const translations = bookNameTranslations[language];
    if (!translations) return bookName;

    // Try exact match first
    if (translations[bookName]) {
        return translations[bookName];
    }

    // Try case-insensitive match
    const lowerBookName = bookName.toLowerCase();
    for (const [englishName, translatedName] of Object.entries(translations)) {
        if (englishName.toLowerCase() === lowerBookName) {
            return translatedName;
        }
    }

    return bookName;
};

// Function to translate chapter field (e.g., "Alma 7" -> "アルマ書 7")
export const translateChapterField = (chapterText, language) => {
    if (!chapterText || !language) return chapterText;

    // Parse the chapter field to separate book name and chapter/verse
    // Matches: "Alma 7", "1 Nephi 3:7", "Isaiah 40:31", "Alma 5:12-14"
    // Also handles numbered books like "1 Nephi", "2 Kings"

    // First, try to match patterns with chapter numbers
    // Pattern: book name (letters/Japanese) + optional space + chapter:verse
    // Handles: "Alma7:11", "Alma 7:11", "2ニーファイ2:15", "1 Nephi 3:7"
    const match = chapterText.match(/^((?:\d\s*)?[A-Za-zぁ-んァ-ン一-龯]+)\s*(\d+(?::\d+(?:-\d+)?)?)$/);

    if (match) {
        const bookName = match[1].trim();
        const chapterVerse = match[2];
        const translatedBook = translateBookName(bookName, language);
        return `${translatedBook} ${chapterVerse}`;
    }

    // If no chapter number, just try to translate the whole thing as a book name
    const translated = translateBookName(chapterText, language);
    return translated;
};
