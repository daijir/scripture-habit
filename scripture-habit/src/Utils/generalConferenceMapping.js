/**
 * Tries to get the title of a talk if mapped.
 * @param {string} year 
 * @param {string} month 
 * @param {string} slug 
 * @param {string} [language='en']
 * @returns {string|null} The title or null
 */
export const getTalkTitle = (year, month, slug, language = 'en') => {
    const key = `${year}/${month}/${slug}`;
    if (language === 'ja') {
        return talkTitlesJa[key] || talkTitles[key] || null;
    }
    return talkTitles[key] || null;
};

/**
 * Tries to map a URL slug to a formatted speaker name.
 * @param {string} slug The slug from the URL (e.g., "15eyre", "nelson")
 * @param {string} [language='en']
 * @returns {string} The formatted name or the cleaned slug if not found.
 */
export const getSpeakerName = (slug, language = 'en') => {
    if (!slug) return '';

    // Remove leading numbers (session codes like "15") and convert to lowercase
    const cleanSlug = slug.replace(/^\d+/, '').replace(/-/g, '').toLowerCase();

    const speakersMap = language === 'ja' ? generalConferenceSpeakersJa : generalConferenceSpeakers;

    // Try exact match first
    if (speakersMap[cleanSlug]) {
        return speakersMap[cleanSlug];
    }

    // Try matching with hyphens preserved
    const slugWithHyphens = slug.replace(/^\d+/, '').toLowerCase();
    if (speakersMap[slugWithHyphens]) {
        return speakersMap[slugWithHyphens];
    }

    // Try partial match
    for (const key in speakersMap) {
        if (cleanSlug.includes(key) || key.includes(cleanSlug)) {
            return speakersMap[key];
        }
    }

    // Fallback: format the slug nicely with "Elder" prefix for unknown speakers
    const textOnly = slug.replace(/^\d+/, '');
    const formattedName = textOnly
        .split('-')
        .map(word => word.charAt(0).toUpperCase() + word.slice(1))
        .join(' ');

    // Add "Elder" prefix for single-word surnames (likely a male speaker)
    // This is a best-guess fallback
    if (!formattedName.includes(' ')) {
        return language === 'ja' ? `${formattedName}長老` : `Elder ${formattedName}`;
    }

    return formattedName;
};
